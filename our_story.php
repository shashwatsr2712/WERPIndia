<div class="container story"><p>Our story began in 2017 with Shitu's perspiration and desire to do an internship outside the four
walls of his research institute in other to have a first-class research work experience in the realworld settings. Before this time, Shitu has been working for 3 years on his Doctoral research
project at Indian Agricultural Research Institute, New Delhi, India. On 22nd August 2017, Shitu
was checking through Victoria (his wife) official college email id to assist her to respond to her
emails when she was busy with house chores. During the process, Shitu discovered an email post
from one of the college student regarding a call to apply for internships from Internshala. This
led to Shitu creating an account on Internshala and immediately applied for 2 Internships
related to his field of research work.</p>
<p>Few days after, he got a message that he has been selected for one of the internships he applied
for. The Internship was Women Empowerment Project of the Forum of Financial Initiatives
(FFI), New Delhi, and was asked to appear for an interview. During the interview, the Managing
Director of FFI, Shri R.K Tripathi shared the vision of empowering women and why it is must be
the core of all sustainable development projects. Since Women Empowerment is directly related
to Shitu Doctoral research project, he gladly accepts the offer to work as an Intern for the
Women Empowerment Project. Shitu joined the Internship on 1st of September, 2017. The
Internship was designed as an Internship Project for 3 months.</p>
<p>Shitu took up the project from scratch and started researching the issues related to Women
oppression and marginalization in India society. During these days of focus tailored research
work, Shitu discovered that Women Empowerment is a complex Social Issue and it can be
solved by Social Research. "As a Social Scientist, I thought of adding Social Research
Dimension to the Women Empowerment Project," said Shitu. The Project began in full force
with the assistance of a few Interns hired by the FFI to work with Shitu, and Shitu became the
Project Director for the project. On Wednesday, 27th December 2017, at the project office, Shitu
discovered he can brand the Project with acronyms "Women Empowerment Research Project
as WERP-India"â€¦And that is how WERP-India brand came to being. Immediately Shitu took
decision with his wife (Victoria) and they decided to buy the domain www.werpindia.org and
started building the website for the new organization. Immediately, the hiring process began and
Shitu took responsibility of Human Resource and manage the hiring process of more interns to
work on the project, WERP-India. Several innovative projects were launched under WERP-India
brand to facilitate finding a solution to the menace of Women and Girls suffering and
marginalization. Among this was the launching of WERPTV, now rebranded as WomenTV to
disseminate the WERP-India project research output and facilitate Women for self-propelling to
empowerment through quality information dissemination. Many of such-like activities based
project were innovated and manage from that time till date.</p>
<p>In August 2018, Shitu came up with the idea for upscaling WERP-India project during a
brainstorming session with the FFI Managing Director on the way forward to achieving the
Vision of Women Empowerment in India. It was decided at the meeting to close down WERPIndia project under FFI and start it newly as an independent organization to give the brand
autonomy to run independently towards achieving the vision of empowering all Indian Women.
On 25th August 2018, The MD FFI, Shri R. K. Tripathi signed documents of the closure of 
WERP-India project under FFI and No Objection for WERP-India to be registered as a private
limited company, a research-based social Enterprise for empowering all India women through
social research and volunteering.</p>
<p>On 15th January 2019, WERP-India was successfully incorporated. WERP-India has since then
run as a student's initiatives for women empowerment in India. A research-based social
enterprise for empowering all Indian Women through social research and volunteering.</p>
